void CGameState::Update()
{

	if (boss.isActive)
	{
		boss.Update();

		for (int i = 0; i < Max; i++)
		{
			bossmonsterleft[i].Update();
		}

		for (int i = 0; i < Max; i++)
		{
			bossmonsterRight[i].Update();
		}
	}

	if (IsKey(VK_F1))
	{
		if (DEAD || boss.hp == 0)
		{
			LIVEUIColor = WHITE;
			hero.hp = 3;
			DEAD = false;
			stateCtrl.StateChange(E_LOGO);

			boss.x = -50;
			boss.y = -50;
			boss.hp = 100;

			hero.x = 60;
			hero.y = 20;
			nCount = 0;
		}
	}
	hero.Update();
	for (int i = 0; i < Max; i++)
	{
		bullet[i].SetPlayerPos(hero.x, hero.y);
		//bullet[i].Col(monster[i].x, monster[i].y, monster[i].isActive);

		monster[i].Update();
		fastmonster[i].Update();
		bullet[i].Update();
		effect[i].Update();
		
	}
	Col();

	if (nCount >= 20)
	{
		for (int i = 0; i < Max; i++)
		{
			bossclipping = 19;
			monster[i].isActive = false;
			monster[i].x = -52456;
			monster[i].y = -23467;
			fastmonster[i].isActive = false;
			fastmonster[i].x = -99898;
			fastmonster[i].y = -246;
			boss.isActive = true;
		}
	}
}

DWORD LifeTimer = GetTickCount();

void CGameState::Col()
{
	for (int i = 0; i < Max; i++)
	{
		for (int j = 0; j < Max; j++)
		{
			if (bullet[i].x == monster[j].x && bullet[i].y <= monster[j].y)
			{
				if (monster[j].isActive && bullet[i].isActive)
				{
					EffectCreate(bullet[i].x, bullet[i].y);
					nCount++;
					bullet[i].isActive = false;
					bullet[i].x = -100;
					bullet[i].y = -100;
					monster[j].isActive = false;
					monster[j].x = -150;
					monster[j].y = -150;
				}
			}

			if (bullet[i].x == fastmonster[j].x && bullet[i].y <= fastmonster[j].y)
			{
				if (fastmonster[j].isActive && bullet[i].isActive)
				{
					nCount += 10;
					EffectCreate(bullet[i].x, bullet[i].y);
					bullet[i].isActive = false;
					bullet[i].x = -100;
					bullet[i].y = -100;
					fastmonster[j].isActive = false;
					fastmonster[j].x = -150;
					fastmonster[j].y = -150;
				}
			}

			if (bullet[i].y <= 17)
			{
				if (boss.isActive && bullet[i].isActive && boss.y >= -1)
				{

					boss.hp -= 1;
					EffectCreate(bullet[i].x, bullet[i].y);
					bullet[i].isActive = false;
					bullet[i].x = -100;
					bullet[i].y = -100;
				}
			}

			if ((hero.x == monster[j].x && hero.y == monster[i].y) || (hero.x == fastmonster[j].x && hero.y == fastmonster[i].y))
			{
				if (monster[j].isActive || fastmonster[j].isActive)
				{
					hero.hp -= 1;
				}
			}

			if (hero.x == bossmonsterleft[j].x && hero.y == bossmonsterleft[j].y)
			{
				if (bossmonsterleft[j].isActive)
				{
					bossmonsterleft[j].isActive = false;
					hero.hp -= 1;
				}
			}

			if (hero.x == bossmonsterRight[j].x && hero.y == bossmonsterRight[j].y)
			{
				if (bossmonsterRight[j].isActive)
				{
					bossmonsterRight[j].isActive = false;
					hero.hp -= 1;
				}
			}
		}

	}
}

